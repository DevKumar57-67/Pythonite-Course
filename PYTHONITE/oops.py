#Object-Oriented Programming (OOP) in Python is a programming paradigm that uses objects and classes to structure software. 
#Instead of focusing on writing functions and logic (like in procedural programming), OOP organizes code around data, making it more modular, reusable, and easier to scale.

#Think of it as a blueprint and building relationship: you design the blueprint once, and then you can build as many houses as you want from it.

# The 4 Pillars of OOP
# To truly master OOP in Python, you need to understand its four fundamental pillars.

# 1. Inheritance
# Inheritance allows a new class (Child/Subclass) to adopt the attributes and methods of an existing class (Parent/Superclass). This prevents code duplication.

#2. Polymorphism
# Polymorphism means "many forms." It allows different classes to have methods with the same name but different implementations.

# 3. Encapsulation
# Encapsulation is the practice of hiding an object's internal data and restricting direct access. In Python, we do this using underscores to denote private or protected attributes.

#_variable: # Protected (a hint to other programmers not to touch it outside the class)

#__variable: # Private (Python actively restricts direct access using name mangling)

#4. Abstraction
# Abstraction hides complex implementation details and only shows the essential features to the user.
#  In Python, this is achieved using the abc (Abstract Base Classes) module. You define a "template" class that cannot be instantiated directly, forcing child classes to implement specific methods.

